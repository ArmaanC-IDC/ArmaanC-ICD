x = float(input("Input a number"))
y = float(input("Input a number"))

#addition
print(f"{x+y}")
#subtraction
print(f"{x-y}")
#multiplication
print(f"{x*y}")
#devision
print(f"{x/y}")

#devision but round the answer down
print(f"{x//y}")
#devision but get the remainder
print(f"{x%y}")
#to the power of
print(f"{x**y}")